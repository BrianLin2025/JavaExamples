
public class EggTwo {
    private String yolkc;
    private String whitec;
    private String type;

    public EggTwo(String yolkc, String whitec, String type) {
        this.yolkc = yolkc;
        this.whitec = whitec;
        this.type = type;
    }

    public String getYolkc() {
        return yolkc;
    }

    public void setYolkc(String yolkc) {
        this.yolkc = yolkc;
    }

    public String getWhitec() {
        return whitec;
    }

    public void setWhitec(String whitec) {
        this.whitec = whitec;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

}
